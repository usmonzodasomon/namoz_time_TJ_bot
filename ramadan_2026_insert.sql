    id SERIAL PRIMARY KEY,
    day_number INTEGER NOT NULL,
    weekday VARCHAR(20) NOT NULL,
    date DATE NOT NULL,
    subh_sadiq TIME NOT NULL,
    shom TIME NOT NULL
);

